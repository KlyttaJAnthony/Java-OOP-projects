

public class MassiveNumber{
  protected double x;
  protected double y;
  protected double z;
  protected double x1;
  protected double y1;


 
  public MassiveNumber(double x, double y, double z, double x1, double y1){
    this.x = x;
    this.y = y;
    this.z = z;
    this.x1 = x1;
    this.y1 = y1;
  }
}
